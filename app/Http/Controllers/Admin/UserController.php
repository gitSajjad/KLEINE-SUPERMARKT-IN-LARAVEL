     */
    public function destroy(User $user)
    {
        $user->delete();
        return redirect()->route('admin.user.index')->with('swal-success',' حذف با موفقیت انجام شد');


    }


    public function setting(User $user)
    {

        $users=($user->permission == '0') ? '1' : '0';
        $user->update(['permission'=>$users]);
        return redirect()->route('admin.user.index')->with('swal-success',' با موفقیت انجام شد');
    }

